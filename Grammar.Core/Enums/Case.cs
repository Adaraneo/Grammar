namespace Grammar.Core.Enums
{
    /// <summary>
    /// Specifies grammatical case values used by inflection requests.
    /// </summary>
    public enum Case
    {
        /// <summary>
        /// Represents the nominative value.
        /// </summary>
        Nominative = 1,
        /// <summary>
        /// Represents the genitive value.
        /// </summary>
        Genitive,
        /// <summary>
        /// Represents the dative value.
        /// </summary>
        Dative,
        /// <summary>
        /// Represents the accusative value.
        /// </summary>
        Accusative,
        /// <summary>
        /// Represents the vocative value.
        /// </summary>
        Vocative,
        /// <summary>
        /// Represents the locative value.
        /// </summary>
        Locative,
        /// <summary>
        /// Represents the instrumental value.
        /// </summary>
        Instrumental
    }
}
