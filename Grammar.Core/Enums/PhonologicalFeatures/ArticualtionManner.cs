        /// Represents the tap or flap value.
        /// </summary>
        TapOrFlap,
        /// <summary>
        /// Represents the fricative value.
        /// </summary>
        Fricative,
        /// <summary>
        /// Represents the lateral fricative value.
        /// </summary>
        LateralFricative,
        /// <summary>
        /// Represents the approximant value.
        /// </summary>
        Approximant,
        /// <summary>
        /// Represents the lateral approximant value.
        /// </summary>
        LateralApproximant,
        /// <summary>
        /// Represents the affricate value.
        /// </summary>
        Affricate
    }
}
