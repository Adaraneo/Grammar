        /// </summary>
        Personal,
        /// <summary>
        /// Represents the possessive value.
        /// </summary>
        Possessive,
        /// <summary>
        /// Represents the demonstrative value.
        /// </summary>
        Demonstrative,
        /// <summary>
        /// Represents the interrogative value.
        /// </summary>
        Interrogative,
        /// <summary>
        /// Represents the relative value.
        /// </summary>
        Relative,
        /// <summary>
        /// Represents the indefinite value.
        /// </summary>
        Indefinite,
        /// <summary>
        /// Represents the negative value.
        /// </summary>
        Negative,
        /// <summary>
        /// Represents the reflexive value.
        /// </summary>
        Reflexive
    }
}
